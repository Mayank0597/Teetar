import React, { useEffect, useState } from "react";
import CardCollection1 from "/src/assets/CardCollection1.png";
import CardCollection2 from "/src/assets/CardCollection2.png";
import CardCollection3 from "/src/assets/CardCollection3.png";
import CardCollection4 from "/src/assets/CardCollection4.png";
import CardCollection5 from "/src/assets/CardCollection5.png";
import CardCollection6 from "/src/assets/CardCollection6.png";
import CardCollection7 from "/src/assets/CardCollection7.png";
import CardCollection8 from "/src/assets/CardCollection8.png";
import Highlight_04 from "/src/assets/Highlight_04.png";
import { Swiper, SwiperSlide } from "swiper/react";
import "swiper/css";
import SwiperCore, { Autoplay } from "swiper";

// Register the Autoplay module
SwiperCore.use([Autoplay]);

const CardCollection = () => {
  const [spaceBetween, setSpaceBetween] = useState(0);

  useEffect(() => {
    const handleResize = () => {
      if (window.innerWidth >= 768 && window.innerWidth < 1024) {
        // For tablet view (768px - 1024px)
        setSpaceBetween(window.innerWidth * 0.05); // 1% of the viewport width
      } else if (window.innerWidth < 768) {
        // For mobile view
        setSpaceBetween(window.innerWidth * 0.02); // 2% of the viewport width
      } else {
        // For other views
        setSpaceBetween(window.innerWidth * 0.02); // 2% of the viewport width
      }
    };

    // Set initial value
    handleResize();

    // Add event listener for window resize
    window.addEventListener("resize", handleResize);

    // Cleanup event listener on component unmount
    return () => window.removeEventListener("resize", handleResize);
  }, []);

  const cards = [
    { src: CardCollection1, alt: "Logo 1", width: "w-24", height: "h-10" },
    { src: CardCollection2, alt: "Logo 2", width: "w-12", height: "h-12" },
    { src: CardCollection3, alt: "Logo 3", width: "w-19", height: "h-7" },
    { src: CardCollection4, alt: "Logo 4", width: "w-12", height: "h-12" },
    { src: CardCollection5, alt: "Logo 5", width: "w-12", height: "h-12" },
    { src: CardCollection6, alt: "Logo 6", width: "w-24", height: "h-10" },
    { src: CardCollection7, alt: "Logo 7", width: "w-12", height: "h-12" },
    { src: CardCollection8, alt: "Logo 8", width: "w-12", height: "h-12" },
  ];

  return (
    <div className="py-12 sm:py-0">
      {/* Grid Layout for Larger Screens */}
      <div className="hidden lg:flex justify-content-center">
        <div className="flex flex-wrap justify-content-center gap-8 w-full max-w-[1000px]">
          {cards.map((card, index) => (
            <div
              key={index}
              className="bg-[#F8F8F3] w-full max-w-[12rem] h-[8rem] rounded-lg flex items-center justify-center border-r-4 border-b-4 border-black border-l-2 border-t-2 hover:border-2"
            >
              <img
                src={card.src}
                alt={card.alt}
                className={`${card.width} ${card.height}`}
              />
            </div>
          ))}
        </div>
      </div>

      {/* Carousel Layout for Mobile Screens */}
      <div className="block sm:hidden container mx-auto -mt-12 sm:-mt-4">
        <Swiper
          spaceBetween={spaceBetween}
          slidesPerView={1.9}
          centeredSlides={true}
          autoplay={{
            delay: 1000,
            disableOnInteraction: false,
          }}
          loop={true}
        >
          {cards.map((card, index) => (
            <SwiperSlide key={index}>
              <div className="bg-[#F8F8F3] w-full max-w-[12rem] h-[8rem] rounded-lg flex items-center justify-center border-r-4 border-b-4 border-black border-l-2 border-t-2 mb-10">
                <img
                  src={card.src}
                  alt={card.alt}
                  className={`${card.width} ${card.height}`}
                />
              </div>
            </SwiperSlide>
          ))}
        </Swiper>
      </div>

      {/* Carousel Layout for Tablet Screens */}
      <div className="hidden sm:block lg:hidden container mx-auto">
        <Swiper
          spaceBetween={spaceBetween}
          slidesPerView={3.2} // Adjust to show parts of the adjacent cards
          centeredSlides={true}
          autoplay={{
            delay: 1000,
            disableOnInteraction: false,
          }}
          loop={true}
        >
          {cards.map((card, index) => (
            <SwiperSlide key={index}>
              <div className="bg-[#F8F8F3] w-full max-w-[15rem] h-[8rem] rounded-lg flex items-center justify-center border-r-4 border-b-4 border-black border-l-2 border-t-2 mb-10">
                <img
                  src={card.src}
                  alt={card.alt}
                  className={`${card.width} ${card.height}`}
                />
              </div>
            </SwiperSlide>
          ))}
        </Swiper>
      </div>

      {/* Highlight Image */}
      <div className="flex justify-end -mt-4 mr-64">
        <img
          src={Highlight_04}
          alt="Highlight"
          className="sm:w-24 sm:h-20 hidden lg:block"
        />
      </div>
    </div>
  );
};

export default CardCollection;
